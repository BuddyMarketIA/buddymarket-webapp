async function run() {
  const recipes = await pool.query('SELECT id, name FROM recipes WHERE "isSeeded" = true ORDER BY id');
  let updated = 0;
  let notFound = 0;

  for (const recipe of recipes.rows) {
    const ingredients = INGREDIENTS[recipe.name];
    if (!ingredients) {
      console.log(`⚠️  Sin ingredientes para: ${recipe.name}`);
      notFound++;
      continue;
    }
    await pool.query(
      'UPDATE recipes SET "ingredientsJson" = $1 WHERE id = $2',
      [JSON.stringify(ingredients), recipe.id]
    );
    updated++;
  }

  console.log(`\n✅ Actualizadas ${updated} recetas con ingredientes`);
  console.log(`⚠️  ${notFound} recetas sin mapeo de ingredientes`);
  await pool.end();
}

run().catch(e => { console.error(e); process.exit(1); });
